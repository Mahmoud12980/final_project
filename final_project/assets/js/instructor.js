//=> Course 1
const courseOneBtn1 = document.getElementById('courseOneBtn1');
const courseOneIf = document.getElementById('courseOneIf');

const showOne = (e) => {
  e.preventDefault();
  courseOneIf.style.display = 'block';
};
courseOneBtn1.addEventListener('dblclick', showOne, false);

const hiddenOne = (e) => {
  e.preventDefault();
  courseOneIf.style.display = 'none';
};
courseOneBtn1.addEventListener('click', hiddenOne, false);



